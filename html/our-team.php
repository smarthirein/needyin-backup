				function changePage(direction) {
					var nextElemIndex = pagesList.indexOf(currentPagerElem) + direction;
					if ((nextElemIndex < 0) || (nextElemIndex > pagesList.length - 1)) {
						return false;
					}
					scrollToElem(jQuery('.team_tree'), function(){
						currentPagerElem = pagesList[nextElemIndex];
						jQuery('.member').each(function(){
							showHideMembPhoto(jQuery(this));
						});
						disablePagerLinks(nextElemIndex);
					});
				}

				function showHideMembPhoto(memberCont) {
					var memberPhoto = memberCont.find('.member_photo'); 
					if (memberPhoto.hasClass('page_' + currentPagerElem)) {
						memberPhoto.addClass('showed');
					} else {
						memberPhoto.removeClass('showed');
					}
				}
				
				function disablePagerLinks(index) {
					if (index == 0) {
						jQuery('.paging_prev').addClass('disabled');
					} else {
						jQuery('.paging_prev').removeClass('disabled');
					}
					if (index == pagesList.length - 1) {
						jQuery('.paging_next').addClass('disabled');
					} else {
						jQuery('.paging_next').removeClass('disabled');
					}
				}

				jQuery(document).ready(function(){
